    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
String nama = jTextField1.getText().trim();
        String username = jTextField3.getText().trim();
        String password = new String(jPasswordField1.getPassword()).trim();
        String noTelp = jTextField2.getText().trim();
        String alamat = jTextArea1.getText().trim();

        // Validasi input
        if (nama.isEmpty() || username.isEmpty() || password.isEmpty() || noTelp.isEmpty() || alamat.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Semua field wajib diisi!", "Peringatan", JOptionPane.WARNING_MESSAGE);
            return;
        }

        if (password.length() < 6) {
            JOptionPane.showMessageDialog(this, "Password minimal 6 karakter!", "Peringatan", JOptionPane.WARNING_MESSAGE);
            return;
        }

        try (Connection conn = DatabaseConfig.getConnection()) {

            // ðŸ”¹ Cek apakah username sudah digunakan
            String cekUser = "SELECT COUNT(*) FROM user WHERE username = ?";
            try (PreparedStatement psCek = conn.prepareStatement(cekUser)) {
                psCek.setString(1, username);
                try (ResultSet rsCek = psCek.executeQuery()) {
                    if (rsCek.next() && rsCek.getInt(1) > 0) {
                        JOptionPane.showMessageDialog(this, "Username sudah digunakan. Gunakan username lain.",
                                "Duplikasi Username", JOptionPane.ERROR_MESSAGE);
                        return;
                    }
                }
            }

            conn.setAutoCommit(false);

            // ðŸ”¹ Simpan data ke tabel user
            String sqlUser = "INSERT INTO user (nama, username, password, no_telepon) VALUES (?, ?, ?, ?)";
            int idUser = 0;
            try (PreparedStatement psUser = conn.prepareStatement(sqlUser, Statement.RETURN_GENERATED_KEYS)) {
                psUser.setString(1, nama);
                psUser.setString(2, username);
                psUser.setString(3, password);
                psUser.setString(4, noTelp);
                psUser.executeUpdate();

                try (ResultSet rs = psUser.getGeneratedKeys()) {
                    if (rs.next()) {
                        idUser = rs.getInt(1);
                    }
                }
            }

            // ðŸ”¹ Simpan ke tabel masyarakat
            String sqlMasyarakat = "INSERT INTO masyarakat (id_user, alamat) VALUES (?, ?)";
            try (PreparedStatement psMas = conn.prepareStatement(sqlMasyarakat)) {
                psMas.setInt(1, idUser);
                psMas.setString(2, alamat);
                psMas.executeUpdate();
            }

            conn.commit();
            JOptionPane.showMessageDialog(this,
                    "Akun berhasil dibuat! Silakan login menggunakan username dan password Anda.",
                    "Registrasi Berhasil", JOptionPane.INFORMATION_MESSAGE);

            new Login().setVisible(true);
            this.dispose();

        } catch (SQLException e) {
            logger.severe("Gagal registrasi: " + e.getMessage());
            JOptionPane.showMessageDialog(this,
                    "Terjadi kesalahan saat menyimpan data.\nDetail: " + e.getMessage(),
                    "Kesalahan Database", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

